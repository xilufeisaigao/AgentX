export function readPreferredLocale() {
  if (typeof window === "undefined") {
    return "zh";
  }
  const params = new URLSearchParams(window.location.search);
  const query = params.get("lang");
  if (query === "zh" || query === "en") {
    return query;
  }
  const stored = window.localStorage.getItem("agentx-ui-locale");
  return stored === "en" ? "en" : "zh";
}

export function translate(locale, key, params) {
  const normalized = locale === "en" ? "en" : "zh";
  const table = dictionaries[normalized];
  return interpolate(table[key] || dictionaries.zh[key] || key, params);
}

export function translateTokenValue(value, locale) {
  if (value === null || value === undefined || value === "") {
    return locale === "zh" ? "未知" : "Unknown";
  }
  const normalized = locale === "en" ? "en" : "zh";
  const raw = String(value);
  if (tokenMap[normalized][raw]) {
    return tokenMap[normalized][raw];
  }
  if (tokenMap[normalized][raw.toUpperCase()]) {
    return tokenMap[normalized][raw.toUpperCase()];
  }
  if (normalized === "zh" && /^[A-Z0-9_]+$/.test(raw)) {
    return raw.toLowerCase().split("_").join(" ");
  }
  return raw
    .toLowerCase()
    .split(/[_-]/)
    .map((part) => part.charAt(0).toUpperCase() + part.slice(1))
    .join(" ");
}

